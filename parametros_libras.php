                <h3 style="color:grey;">4° Expressão facial e/ou corporal</h3>
                    <h5>Esse parâmetro é de fundamental importância na linguagem de Libras, pois permite a transmissão de sentimento e intensidade. Incluem o uso de expressões faciais, linguagem corporal, movimentos da cabeça, olhares, etc.</h5>
                    <h5>Se uma pessoa quer demonstrar que está com raiva de alguém ou de algo, talvez não precise usar nem um sinal. Basta utilizar apenas a expressão facial. Ou, se alguém fizer uma pergunta para responder "sim" ou "não", basta simplesmente balançar a cabeça de acordo. Estas são simples situações para exemplificar este parâmetro, todavia, durante uma conversa em Libras, é necessário combinar diversos componentes não manuais com sinais específicos para esclarecer a mensagem.</h5>
