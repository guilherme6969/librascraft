                <h3 style="color:grey;">2° Ponto de Articulação</h3>
                    <h5>É o lugar onde incide a mão predominante configurada, podendo tocar alguma parte do corpo, como também estar em um espaço neutro vertical ou horizontal. É o espaço em frente ao corpo ou uma região do próprio corpo, onde os sinais são articulados.</h5>
                    <h5> Os sinais articulados no espaço são de dois tipos: os que se articulam no espaço neutro diante do corpo e os que se aproximam de uma determinada região do corpo, como, por exemplo, a cabeça, a cintura e os ombros.</h5>
                    <br />
