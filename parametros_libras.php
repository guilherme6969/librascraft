                    <br />

                <h3 style="color:grey;">4° Orientação/direcionalidade</h3>
                    <h5>Orientação é a direção para a qual a palma da mão aponta na produção do sinal. Alguns sinais têm a mesma configuração, o mesmo ponto de articulação e o mesmo movimento, e diferem apenas na orientação da mão. É importante perceber como a modificação de um único parâmetro pode alterar completamente o significado do sinal.</h5>
                    <h5>Os verbos IR e VIR se opõem em relação à direcionalidade, como os verbos SUBIR e DESCER, ACENDER e APAGAR, ABRIR-PORTA e FECHAR-PORTA.</h5>
