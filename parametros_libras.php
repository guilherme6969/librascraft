                <h3 style="color:grey;">Introdução</h3>
                    <h5>
                        As línguas que as comunidades surdas do mundo desenvolveram, passam por processos de denominação um pouco diferentes, embora comparáveis em alguns pontos aos das outras línguas orais.<br />
                        Quando falamos sobre os articuladores da língua de sinais, certamente podemos pensar em mãos. Mas na realidade, são usados como articuladores, além de mãos, outras partes do corpo, como a cabeça, face e tronco.
                    </h5>
                <h4 style="color:grey;" >Quais são os cinco parâmetros formativos das Libras?</h4>
                    <h5>1° A configuração da mão;</h5>
                    <h5>2° Ponto ou local de articulação;</h5>
                    <h5>3° O movimento;</h5>
                    <h5>4° Orientação/direcionalidade;</h5>
                    <h5>5° Expressão facial e/ou corporal;</h5>
                <h4 style="color:grey;">Vamos conhecer cada um dos parâmetros:</h4>
                    <br />
                    <br />

                <h3 style="color:grey;">1° A Configuração da Mão</h3>
                    <h5>São as diversas formas que uma ou as duas mãos tomam na realização do sinal. Comparando-se as configurações utilizadas em LIBRAS e em ASL (American Sign Language), percebe-se que há um grande número de similaridades e algumas diferenças. Por essa divergência entre as duas línguas pode-se notar que cada língua possui seu sistema de configurações e que estas não se restringem apenas às nomeadas no alfabeto manual.</h5>
                    <h5>Cada configuração pode ser feita pela mão dominante (mão direita para os destros, mão esquerda para os canhotos), ou pelas duas mãos dependendo do sinal.</h5>
                    <h5> Os sinais APRENDER, SÁBADO, LARANJA e DESODORANTE-SPRAY têm a mesma configuração de mão e são realizados na testa, na boca e na axila, respectivamente.</h5>
