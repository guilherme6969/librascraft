                    <h5>Deve-se dizer que no discurso normal as extremidades são articuladas em um espaço mais limitado que a extensão máxima, portanto, o tamanho do sinal pode ser comparado à intensidade da voz.</h5>
                    <br />
                    <br />
                
                <h3 style="color:grey;">3° O Movimento</h3>
                    <h5>É um parâmetro tão complexo que pode envolver uma grande quantidade de formas e direções, desde os movimentos internos da mão, os movimentos do pulso, movimentos direcionais no espaço e até conjuntos de movimentos no mesmo sinal.</h5>
                    <h5>Dessa forma, podemos entender que o parâmetro de movimento refere-se ao modo como as mãos se movimentam (movimento linear, em movimento da forma de seta arqueada, circular, simultânea ou alternada com ambas as mãos, etc.) e para onde estão movimentando (para a frente, em direção à direita, esquerda, etc...).</h5>
